// adicionar elemento no array

// push
// -> método que adiciona no final do array
const notas = [10, 6, 8];
notas.push(7);

console.log(notas);

// unshift()
// -> método que adiciona um novo elemento na primeira posição do array

const outrasNotas = [10, 6, 8];
outrasNotas.unshift(7);

console.log(outrasNotas);