// remover um elemento do array

// pop
// -> método que remove o último elemento do array
// -> não aceita parâmetro
const notas = [10, 6, 8];
notas.pop();

console.log(notas);

// shift()
// -> método que remove o primeiro elemento do array
// troca o índice de todos os elementos para um a menos do que eram

const outrasNotas = [10, 6, 8];
outrasNotas.shift();

console.log(outrasNotas);